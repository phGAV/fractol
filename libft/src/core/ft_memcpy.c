#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	unsigned char	*d;
	unsigned char	*s;

	d = (unsigned char*)dst;
	s = (unsigned char*)src;
	if (src == NULL && dst == NULL)
		return (NULL);
	while (n--)
		*d++ = *s++;
	return (dst);
}
